package apply.model;

public class RankDto {


	private String month;
	private String subject;
	private int count;
	private int nu_count;
	private int sum;
	
	
	
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getNu_count() {
		return nu_count;
	}
	public void setNu_count(int nu_count) {
		this.nu_count = nu_count;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	
	
	
	
	
}
